package ac7week1.ac0711.for_1;

/*
    for 문
    초기값 , 조건식, 증감식이 한 곳에 모여 있는 구문
    횟수 파악이 용이하기 때문에, 지정 횟수 반복에 자주 사용되며
    뒤에 배울 배열 혹은 컬렉션의 반복에 자주 사용된다.
 */
public class Ex06 {
    public static void main(String[] args) {

        for(int i = 1; i <= 5; i++){
            System.out.println("hello world");
        }
        /*
            for 문의 동작 순서
            초기값 -> 조건식 비교 -> 종속문 -> 증감식 -> 조건식 ->...

         */

    }
}
